#include "libft.h"

void	ft_putnbr(int n)
{
	char	str[1];

	if (n < 0)
	{
		write(1, "-", 1);
		if (n == -2147483648)
		{
			ft_putnbr(-(n / 10));
			ft_putnbr(8);
			return ;
		}
		ft_putnbr(-n);
		return ;
	}
	str[0] = n % 10 + 48;
	if (n / 10)
		ft_putnbr(n / 10);
	write(1, str, 1);
}
